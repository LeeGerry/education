package edu.auburn.domain;

public class ExamResult {
	private int rid;
	private int uid;
	private int eid;
	private String answer;
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	@Override
	public String toString() {
		return "ExamResult [uid=" + uid + ", eid=" + eid + ", answer=" + answer + "]";
	}
	
}
